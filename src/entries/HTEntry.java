package entries;

public class HTEntry {
	
	public FinalEntry fe;
	public float zero;
	public float one;
	public float two;
	public float more;
	
	public HTEntry(FinalEntry fe, float zero, float one, float two, float more) {
		super();
		this.fe = fe;
		this.zero = zero;
		this.one = one;
		this.two = two;
		this.more = more;
	}
	

}
