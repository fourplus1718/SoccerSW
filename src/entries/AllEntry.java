package entries;

public class AllEntry {
	public FinalEntry fe;
	public float zero;
	public float one;
	public float two;
	public float more;
	public float basic;
	public float poisson;
	public float weighted;
	public float shots;

	public AllEntry(FinalEntry fe, float zero, float one, float two, float more, float basic, float poisson,
			float weighted, float shots) {
		super();
		this.fe = fe;
		this.zero = zero;
		this.one = one;
		this.two = two;
		this.more = more;
		this.basic = basic;
		this.poisson = poisson;
		this.weighted = weighted;
		this.shots = shots;
	}


}
