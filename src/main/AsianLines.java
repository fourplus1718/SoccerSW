package main;

public class AsianLines {

	public Line line1;
	public Line line2;
	public Line main;
	public Line line3;
	public Line line4;

	public AsianLines(Line main) {
		super();
		this.main = main;
	}

	public AsianLines(Line line1, Line line2, Line main, Line line3, Line lien4) {
		super();
		this.line1 = line1;
		this.line2 = line2;
		this.main = main;
		this.line3 = line3;
		this.line4 = lien4;
	}

}
